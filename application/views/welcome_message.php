                          FedEx Corporation es una empresa aérea y logística estadounidense, que opera a nivel internacional.
                          La compañía se fundó bajo la denominación Federal Express en 1971 por Frederick W. Smith en Little Rock (Arkansas). Por tanto, Fedex es la abreviación del nombre original de la compañía Federal Express.
                          Frederick W. Smith estudiaba en la Universidad de Yale cuando escribió, como parte de las tareas académicas encomendadas un reporte sobre las compañías de correo y su ineficiente funcionamiento que originaba retrasos en la entrega de cartas y documentos.
                          Esto se debía a que los transportistas centraban sus esfuerzos en el transporte de grandes paquetes en Estados Unidos.
                          Smith pensó que una empresa que llevará pequeños paquetes en tamaño, pero esenciales (en importancia y en urgencia) podía constituirse en un transportista más eficiente que las empresas que existían por aquel entonces.
                          La compañía se fundó bajo la denominación Federal Express en 1971 por Frederick W. Smith en Little Rock (Arkansas).
                          En 1973 se traslada Memphis (Tennessee) e inicia sus operaciones. En 1985 arranca sus operativas en territorio europeo con sus vuelos regulares.
                          En 1989 la marca está presente con operaciones directas en países árabes como Dubái y Emiratos Árabes Unidos. En este mismo año, la compañía adquiere Flying Tigers para incrementar su presencia internacional.
                          En 1991 se lanza Express Freighter para ofrecer entregas a la mañana siguiente entre los mercados de EE.UU., Europa y Asia.
                          En 1994 la marca lanza fedex.com, el primer sitio Web de transporte que ofrece el servicio de seguimiento de la situación de paquetes enviados de forma online.
                          En 1996 el lanzamiento de FedEx Ship Manager (en sus inicios, FedEx interNetShip) convierte a la compañía en la primera en permitir a sus clientes procesar los envíos online.
                          Actualmente la marca intenta mantener una política de cuidado y compromiso por el medio ambiente palpable, como por ejemplo con su vanguardista centro de distribución ecosostenible en Machelen, Bélgica.
